package com.class2Vaiable;

public class ArithmeticOperaters {

	public static void main(String[] args) {

		// Declare 2 integer variables and then perform:
		// addition,subtraction,multiplication,divisions

		int a = 30;
		int b = 15;

		System.out.println(a + b);// 45
		System.out.println(a - b); // 15
		System.out.println(a * b); // 450
		System.out.println(a / b); // 2

		int sum = a + b;
		int sub = a - b;
		int mult = a * b;
		int div = a / b;

		System.out.println(sum);// 45
		System.out.println(sub);// 15
		System.out.println(mult);// 450
		System.out.println(div);// 2

		// print:the Addition/subtraction of 2 numbers is_____

		System.out.println("addition of 2 numbers is " + sum);

		System.out.println("subtraction of 2 numbers is " + sub);

	}
}
